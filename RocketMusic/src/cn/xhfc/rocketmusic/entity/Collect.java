package cn.xhfc.rocketmusic.entity;

public class Collect {
    private Integer colId;

    private Integer colUser;

    private Integer colSong;

    private Integer colAlbum;

    private Integer colList;

    private Integer colSinger;

    private Integer colMv;

    public Integer getColId() {
        return colId;
    }

    public void setColId(Integer colId) {
        this.colId = colId;
    }

    public Integer getColUser() {
        return colUser;
    }

    public void setColUser(Integer colUser) {
        this.colUser = colUser;
    }

    public Integer getColSong() {
        return colSong;
    }

    public void setColSong(Integer colSong) {
        this.colSong = colSong;
    }

    public Integer getColAlbum() {
        return colAlbum;
    }

    public void setColAlbum(Integer colAlbum) {
        this.colAlbum = colAlbum;
    }

    public Integer getColList() {
        return colList;
    }

    public void setColList(Integer colList) {
        this.colList = colList;
    }

    public Integer getColSinger() {
        return colSinger;
    }

    public void setColSinger(Integer colSinger) {
        this.colSinger = colSinger;
    }

    public Integer getColMv() {
        return colMv;
    }

    public void setColMv(Integer colMv) {
        this.colMv = colMv;
    }
}